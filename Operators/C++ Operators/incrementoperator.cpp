//Increment Operantor
#include<iostream>
using namespace std;

int main ()

{

    int a,b;

    a= 15;
    b=34;

    cout<<"Before incremet: "<<" A= "<<a<<" B= "<<b;

    ++a; //a=a+1,  a=15+1
    b++;//B=b+1,  b=34+1

    
    cout<<"\nAfter incremet: "<<" A= "<<a<<" B= "<<b;
    
    return 0;

}