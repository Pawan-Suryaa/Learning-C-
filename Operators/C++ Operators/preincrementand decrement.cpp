//Pre Increment Operator
#include<iostream>
using namespace std;

int main ()
{
     int a,b,c,d;

     a=10;
     b=20;

     cout<<"Before Pre Increment: "<<"A= "<<a<<"B= "<<b;

     c=++a;
     d=++b;

     cout<<"After Pre Increment: "<<"A= "<<a<<"B= "<<b;

     cout<<"Before Pre Increment: "<<"C= "<<c<<"D= "<<d;

    return 0;
}