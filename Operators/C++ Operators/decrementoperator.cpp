//Decrement Operantor
#include<iostream>
using namespace std;

int main ()

{

    int a,b;

    a= 15;
    b=34;

    cout<<"Before Decrement: "<<" A= "<<a<<" B= "<<b;

    --a; //A=a-1,  A=15-1, A=14
    b--;//B=b-1,  b=34-1, B=34

    
    cout<<"\nAfter Decrement: "<<" A= "<<a<<" B= "<<b;
    
    return 0;

}