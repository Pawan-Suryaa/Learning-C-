//post Decrement Operator
#include<iostream>
using namespace std;

int main ()
{
     int a,b,c,d;

     a=10;
     b=20;

     cout<<"Before post Decerement: "<<"A= "<<a<<" B= "<<b;

     c=a--;
     d=b--;

     cout<<"\nAfter post Decrement: "<<" A= "<<a<<" B= "<<b;

     cout<<"\nafter post Decrement: "<<" C= "<<c<<" D= "<<d;

    return 0;
}